    private void btnLiberarVeiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLiberarVeiculoActionPerformed
        AlugadosDAO alugados = new AlugadosDAO();
        if (qtdToDo >= 1) {
            String rs = alugados.remove(gAlugados.getPlaca());

            if (!rs.equals("ok")) {
                JOptionPane.showMessageDialog(null, rs);
                return;
            }

            // Carrega items
            loadItems("", "");

            JOptionPane.showMessageDialog(null, "Veiculo liberado com sucesso!");
        }
    }//GEN-LAST:event_btnLiberarVeiculoActionPerformed

