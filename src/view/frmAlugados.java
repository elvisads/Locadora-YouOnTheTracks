    /**
     * Metodo que carrega os ToDo
     *
     * @author Flavio Ever
     * @param placa placa do carro
     * @param cpf cpf do cliente
     * @since 10/05/2020
     */
    public void loadItems(String placa, String cpf) {
        qtdToDo = 0;

        String titulos[] = {"Placa", "Marca", "Modelo", "Preço Total", "Data Aluguel", "Data Retorno", "CPF", "Nome"};

        DefaultTableModel modelGrid = new DefaultTableModel(null, titulos) {
            @Override
            public boolean isCellEditable(int row, int column) {
                // Desabilita edição da jTable
                return false;
            }
        };

        alugadosRequest = (ArrayList<Alugados>) new AlugadosDAO().index(placa, cpf);

        for (int i = 0; i < alugadosRequest.size(); i++) {
            modelGrid.addRow(new Object[]{
                alugadosRequest.get(i).getPlaca(),
                alugadosRequest.get(i).getMarca(),
                alugadosRequest.get(i).getModelo(),
                "R$"+ alugadosRequest.get(i).getPrecoTotal(),
                alugadosRequest.get(i).getDataAluguel(),
                alugadosRequest.get(i).getDataRetorno(),
                alugadosRequest.get(i).getCpf(),
                alugadosRequest.get(i).getNome()
            });
            qtdToDo++;
        }

        lbQtd.setText(qtdToDo.toString());
        tbAlugados.setModel(modelGrid);
    }

