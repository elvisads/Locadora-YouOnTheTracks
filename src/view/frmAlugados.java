    private void tbAlugadosMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbAlugadosMouseReleased
         if (tbAlugados.getSelectionModel().isSelectionEmpty()) {
            JOptionPane.showMessageDialog(null, "Nada selecionado");
        } else {
            int index = tbAlugados.getSelectionModel().getMinSelectionIndex();
            
            // Referencia pra Dropar
            gAlugados.setPlaca(alugadosRequest.get(index).getPlaca());
        }
    }//GEN-LAST:event_tbAlugadosMouseReleased

